import mongoose from "mongoose";

const AllergyAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "allergy_id": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "allergy_answers"
  }
);

const AllergyAnswers = mongoose.model("AllergyAnswers", AllergyAnswersSchema);
export default AllergyAnswers;
