import mongoose from "mongoose";

const AttentionFocusSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "name": {
      type: String,
      required: true,
    },
    "code": {
      type: String,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "attention_focus"
  }
);

const AttentionFocus = mongoose.model("AttentionFocus", AttentionFocusSchema);
export default AttentionFocus;
