import mongoose from "mongoose";

const AttentionStatesSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "name": {
      type: String,
      required: true,
    },
    "code": {
      type: String,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "attention_states"
  }
);

const AttentionStates = mongoose.model("AttentionStates", AttentionStatesSchema);
export default AttentionStates;
