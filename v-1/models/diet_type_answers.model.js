import mongoose from "mongoose";

const DietTypeAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "diet_type_id": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "diet_type_answers"
  }
);

const DietTypeAnswers = mongoose.model("DietTypeAnswers", DietTypeAnswersSchema);
export default DietTypeAnswers;
