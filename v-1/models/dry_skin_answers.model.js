import mongoose from "mongoose";

const DrySkinAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "dry_skin_id": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "dry_skin_answers"
  }
);

const DrySkinAnswers = mongoose.model("DrySkinAnswers", DrySkinAnswersSchema);
export default DrySkinAnswers;
