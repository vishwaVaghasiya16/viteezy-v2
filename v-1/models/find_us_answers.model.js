import mongoose from "mongoose";

const FindUsAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "find_us_id": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "find_us_answers"
  }
);

const FindUsAnswers = mongoose.model("FindUsAnswers", FindUsAnswersSchema);
export default FindUsAnswers;
