import mongoose from "mongoose";

const GenderAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "gender_id": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "gender_answers"
  }
);

const GenderAnswers = mongoose.model("GenderAnswers", GenderAnswersSchema);
export default GenderAnswers;
