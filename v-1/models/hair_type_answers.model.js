import mongoose from "mongoose";

const HairTypeAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "hair_type_id": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "hair_type_answers"
  }
);

const HairTypeAnswers = mongoose.model("HairTypeAnswers", HairTypeAnswersSchema);
export default HairTypeAnswers;
