import mongoose from "mongoose";

const HeightAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "height_cm": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "height_answers"
  }
);

const HeightAnswers = mongoose.model("HeightAnswers", HeightAnswersSchema);
export default HeightAnswers;
