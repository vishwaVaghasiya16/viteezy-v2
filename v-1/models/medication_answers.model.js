import mongoose from "mongoose";

const MedicationAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "medication_id": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "medication_answers"
  }
);

const MedicationAnswers = mongoose.model("MedicationAnswers", MedicationAnswersSchema);
export default MedicationAnswers;
