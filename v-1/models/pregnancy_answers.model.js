import mongoose from "mongoose";

const PregnancyAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "pregnancy_id": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "pregnancy_answers"
  }
);

const PregnancyAnswers = mongoose.model("PregnancyAnswers", PregnancyAnswersSchema);
export default PregnancyAnswers;
