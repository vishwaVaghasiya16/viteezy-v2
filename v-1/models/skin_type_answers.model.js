import mongoose from "mongoose";

const SkinTypeAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "skin_type_id": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "skin_type_answers"
  }
);

const SkinTypeAnswers = mongoose.model("SkinTypeAnswers", SkinTypeAnswersSchema);
export default SkinTypeAnswers;
