import mongoose from "mongoose";

const SmokesAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "smoke_id": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "smokes_answers"
  }
);

const SmokesAnswers = mongoose.model("SmokesAnswers", SmokesAnswersSchema);
export default SmokesAnswers;
