import mongoose from "mongoose";

const SportAmountAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "sport_amount_id": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "sport_amount_answers"
  }
);

const SportAmountAnswers = mongoose.model("SportAmountAnswers", SportAmountAnswersSchema);
export default SportAmountAnswers;
