import mongoose from "mongoose";

const SportReasonAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "sport_reason_id": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "sport_reason_answers"
  }
);

const SportReasonAnswers = mongoose.model("SportReasonAnswers", SportReasonAnswersSchema);
export default SportReasonAnswers;
