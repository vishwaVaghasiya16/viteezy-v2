import mongoose from "mongoose";

const SportTypesAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "sport_type_id": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "sport_types_answers"
  }
);

const SportTypesAnswers = mongoose.model("SportTypesAnswers", SportTypesAnswersSchema);
export default SportTypesAnswers;
