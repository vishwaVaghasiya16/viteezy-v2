import mongoose from "mongoose";

const TypeOfTrainingsSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "name": {
      type: String,
      required: true,
    },
    "code": {
      type: String,
      required: true,
    },
    "subtitle": {
      type: String,
      required: false,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
    "is_active": {
      type: Boolean,
      required: false,
      default: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "type_of_trainings"
  }
);

const TypeOfTrainings = mongoose.model("TypeOfTrainings", TypeOfTrainingsSchema);
export default TypeOfTrainings;
