import mongoose from "mongoose";

const UsedSupplementsSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "name": {
      type: String,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
    "code": {
      type: String,
      required: true,
    },
    "is_active": {
      type: Boolean,
      required: false,
      default: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "used_supplements"
  }
);

const UsedSupplements = mongoose.model("UsedSupplements", UsedSupplementsSchema);
export default UsedSupplements;
