import mongoose from "mongoose";

const WeightAnswersSchema = new mongoose.Schema(
  {
    "id": {
      type: Number,
      required: true,
    },
    "quiz_id": {
      type: Number,
      required: true,
    },
    "weight_gr": {
      type: Number,
      required: true,
    },
    "creation_timestamp": {
      type: Date,
      required: true,
    },
    "modification_timestamp": {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: false,
    versionKey: false,
    collection: "weight_answers"
  }
);

const WeightAnswers = mongoose.model("WeightAnswers", WeightAnswersSchema);
export default WeightAnswers;
